//3) Write a program to create a character array containing the contents of a string
public class Asi3 {

	public static void main(String[] args)
	{
		String str="ADITYA";
		char[] ch=str.toCharArray();
		for (int i = 0; i < ch.length; i++)
		{
			System.out.print(ch[i]+" ");
		}
	}

}
